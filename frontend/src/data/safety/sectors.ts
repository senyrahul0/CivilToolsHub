export function getSafetySector(sectorSlug: string) {
  return safetySectors.find(
    (sector) => sector.slug === sectorSlug,
  );
}

export function getSafetySectorStats(sector: SafetySector) {
  return {
    totalAreas: sector.areas.length,
    totalActivities: sector.areas.reduce(
      (total, area) => total + area.activities.length,
      0,
    ),
    totalHazards: sector.areas.reduce(
      (total, area) => total + area.hazards.length,
      0,
    ),
  };
}

